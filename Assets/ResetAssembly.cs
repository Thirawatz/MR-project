using MixedReality.Toolkit.UX;
using System.Collections.Generic;
using UnityEngine;

public class ResetAssembly : MonoBehaviour
{
    public List<GameObject> assemblyParts; // ??????????????????????????????
    public Transform[] initialPositions;   // ???????????????????????????????????
    public PressableButton pressableButton; // ?????????????????????????

    void Start()
    {
        if (pressableButton != null)
        {
            // ????????????? reset ??????????????
            pressableButton.OnClicked.AddListener(ResetParts); // ??? OnClicked ?????
        }
        else
        {
            Debug.LogWarning("PressableButton is not assigned.");
        }
    }

    public void ResetParts()
    {
        if (assemblyParts.Count != initialPositions.Length)
        {
            Debug.LogWarning("The number of assembly parts does not match the number of initial positions.");
            return;
        }

        for (int i = 0; i < assemblyParts.Count; i++)
        {
            // ????????????????????????????????????????????
            assemblyParts[i].transform.position = initialPositions[i].position;
            assemblyParts[i].transform.rotation = initialPositions[i].rotation;
        }
    }
}
